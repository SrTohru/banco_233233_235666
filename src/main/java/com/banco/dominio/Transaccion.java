package com.banco.dominio;

public class Transaccion {
    String estado, fecha, tipo;
    int id, idCliente;
    double monto;

    public Transaccion() {
    }

    public Transaccion(String estado, String tipo,int id, String fecha, int idCliente, double monto) {
        this.estado = estado;
        this.tipo = tipo;
        this.id = id;
        this.fecha = fecha;
        this.idCliente = idCliente;
        this.monto = monto;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public int getIdCliente() {
        return idCliente;
    }

    public void setIdCliente(int idCliente) {
        this.idCliente = idCliente;
    }

    public double getMonto() {
        return monto;
    }

    public void setMonto(double monto) {
        this.monto = monto;
    }
    
    
}
